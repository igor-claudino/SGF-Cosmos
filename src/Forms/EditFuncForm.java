    private void jb_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_cadastrarActionPerformed
        // TODO add your handling code here:
        Funcionario func = new Funcionario();

        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        func.setNome(tf_nome.getText().toUpperCase());
        func.setAdmissao(ff_data_admissao.getText());
        func.setCalcado(String.valueOf(cb_calcado.getSelectedItem()));
        func.setCpf(ff_cpf.getText());
        func.setCtps(Integer.parseInt(tf_ctps.getText()));
        func.setData_expedicao(ff_data_expedicao.getText());
        func.setData_nasc(ff_data_nasc.getText());
        func.setDependentes(tf_num_dependentes.getText());
        func.setEndereco(tf_endereco.getText().toUpperCase());
        func.setEst_civil(String.valueOf(cb_estado_civil.getSelectedItem()));
        func.setFuncao(tf_funcao.getText().toUpperCase());
        func.setFuncao_atual(tf_funcao_atual.getText().toUpperCase());
        func.setGrau_instru(String.valueOf(cb_grau_intrucao.getSelectedItem()));
        func.setLoja_atual(tf_loja_atual.getText().toUpperCase());
        func.setNome_pai(tf_nome_pai.getText().toUpperCase());
        func.setNome_mae(tf_nome_mae.getText().toUpperCase());
        func.setOrg_expeditor(tf_org_expeditor.getText().toUpperCase());
        func.setPis(tf_pis.getText());
        func.setRg(tf_rg.getText());
        func.setSerie(tf_serie.getText().toUpperCase());
        func.setSexo(sexo);
        System.out.println(func.getSexo());
        func.setTelefone1(ff_telefone1.getText());
        func.setTelefone2(ff_telefone2.getText());
        func.setUniforme(String.valueOf(cb_uniforme.getSelectedItem()));
        func.setId(id);
        FuncionarioDAO dao;
        try {
            dao = new FuncionarioDAO();
            dao.editarFuncionario(func);
            dispose();
            new FuncionarioForm().show();
        } catch (SQLException ex) {
            Logger.getLogger(NovoFuncForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro", "Erro ao cadastrar funcionário!!", JOptionPane.ERROR_MESSAGE);
        } catch (ParseException ex) {
            Logger.getLogger(NovoFuncForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(EditFuncForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jb_cadastrarActionPerformed

