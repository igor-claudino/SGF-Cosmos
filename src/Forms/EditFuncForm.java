    public void carregarCamposEspeciais(){
        int i = 0;
        while(i < cb_grau_intrucao.getItemCount()){
            
           try{
                if(f1.getGrau_instru().equals(cb_grau_intrucao.getItemAt(i))){
                cb_grau_intrucao.setSelectedIndex(i);
                break;
                }else{
                    i++;
                }   
            }catch(Exception ex){
                    cb_grau_intrucao.setSelectedIndex(0);
                    break;
            }   
           
            
        }
        int di = 0;
        
        while(di < cb_calcado.getItemCount()){
            
            try{
                if(f1.getCalcado().equals(cb_calcado.getItemAt(di))){
                cb_calcado.setSelectedIndex(di);
                break;
            }else{
                    di++;
                }
            }catch(Exception ex){
                cb_calcado.setSelectedIndex(0);
                break;
            }
            
        }
        
        i = 0;
        while(i < cb_estado_civil.getItemCount()){
            
            try{
                if(f1.getEst_civil().equals(cb_estado_civil.getItemAt(i))){
                
                cb_estado_civil.setSelectedIndex(i);
                break;
            }
                else{
                    i++;
                }
            }catch(Exception ex){
                cb_estado_civil.setSelectedIndex(0);
                break;
            }
            
        }
        
        i = 0;
        while(i < cb_uniforme.getItemCount()){
            
            try{
                if(f1.getUniforme().equals(cb_uniforme.getItemAt(i))){
                cb_uniforme.setSelectedIndex(i);
                break;
            }else{
                    i++;
                }
            }catch(Exception ex){
                cb_uniforme.setSelectedIndex(0);
                break;
            }
            
        }
        
        if(f1.getSexo().equals("Masculino")){
            rb_masc.setSelected(true);
        }else if(f1.getSexo().equals("Feminino")){
            rb_fem.setSelected(true);
        }
        
    }
    public void definirSexo(){
        if(rb_masc.isSelected()){
            sexo = "Masculino";
            
        }else if(rb_fem.isSelected()){
            sexo = "Feminino";
        }
    }
    
  

