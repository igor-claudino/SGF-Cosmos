    private void tf_pesqKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_pesqKeyReleased
        // TODO add your handling code here:
        if (tf_pesq.getText().equals("")) {
            try {
                preencherTabela();
            } catch (IOException ex) {
                Logger.getLogger(SuspensaoForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            List<Suspensoes> filterList = new LinkedList<Suspensoes>();
            for (Suspensoes suspensoes : minhaList) {
                if (suspensoes.getFuncionario().getNome().contains(tf_pesq.getText().toUpperCase())) {
                    filterList.add(suspensoes);
                }
                
                try {
                    minhaList = filterList;
                    preencherTabela(minhaList);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Erro" + ex);
                }
            }
        }
    }//GEN-LAST:event_tf_pesqKeyReleased

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Suspensoes susp = new Suspensoes();
        SuspensaoDAO dao;
        try {
            dao = new SuspensaoDAO();
        
        if (tb_func_susp.getSelectedRow() == -1) {
           JOptionPane.showMessageDialog(null, "Selecione um registro", "Erro", JOptionPane.ERROR_MESSAGE);
           return;
       }
        
        
        if (JOptionPane.showConfirmDialog(null, "Deseja mesmo excluir?", "confirmação", 
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
        
            try {
                susp =(Suspensoes) minhaList.get(tb_func_susp.getSelectedRow());
                dao.excluirSusp(susp);
                tf_pesq.setText("");
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
         } catch (SQLException ex) {
            Logger.getLogger(FuncionarioForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(SuspensaoForm.class.getName()).log(Level.SEVERE, null, ex);
        }                      
        try {
            preencherTabela();
        } catch (IOException ex) {
            Logger.getLogger(SuspensaoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

