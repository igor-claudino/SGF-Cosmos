    public void preencherTabela() throws IOException {
        try {
            SuspensaoDAO dao = new SuspensaoDAO();
            minhaList = dao.List();
            MyTableModel mtb = new MyTableModel(Suspensoes.class, minhaList, tb_func_susp);
            tb_func_susp.setModel(mtb);
            tb_func_susp.getColumnModel().getColumn(0).setPreferredWidth(300);
            tb_func_susp.getColumnModel().getColumn(1).setPreferredWidth(100);
            tb_func_susp.getColumnModel().getColumn(2).setPreferredWidth(100);
            tb_func_susp.getColumnModel().getColumn(3).setPreferredWidth(300);
        } catch (SQLException ex) {
            Logger.getLogger(Funcionario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(SuspensaoForm.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }
    
