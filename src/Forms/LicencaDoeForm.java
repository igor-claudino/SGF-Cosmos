    public void preencherTabela() throws ParseException, IOException{
        try {
            LicencaDoencaDAO dao = new LicencaDoencaDAO();
            minhaList = dao.List();
            MyTableModel mtb = new MyTableModel(LicencaDoenca.class, minhaList, tb_licdoe);
            tb_licdoe.setModel(mtb);
            tb_licdoe.getColumnModel().getColumn(0).setPreferredWidth(300);
            tb_licdoe.getColumnModel().getColumn(1).setPreferredWidth(100);
            tb_licdoe.getColumnModel().getColumn(2).setPreferredWidth(100);
        } catch (SQLException ex) {
            Logger.getLogger(LicencaDoenca.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
