    private void tf_pesqKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_pesqKeyReleased
        // TODO add your handling code here:
        if (tf_pesq.getText().equals("")) {
            try {
                preencherTabela();
            } catch (ParseException ex) {
                System.out.println(ex);
            } catch (SQLException ex) {
                Logger.getLogger(LicencaMatForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(LicencaMatForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            List<LicencaMaternidade> filterList = new LinkedList<LicencaMaternidade>();
            for (LicencaMaternidade maternidade : minhaList) {
                if (maternidade.getFuncionario().getNome().contains(tf_pesq.getText().toUpperCase())) {
                    filterList.add(maternidade);
                }
                
                try {
                    minhaList = filterList;
                    preencherTabela(minhaList);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Erro" + ex);
                }
            }
        }
    }//GEN-LAST:event_tf_pesqKeyReleased

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        LicencaMaternidade licm = new LicencaMaternidade();
        LicencaMaternidadeDAO dao;
        try {
            dao = new LicencaMaternidadeDAO();
        
        if (tb_func_mat.getSelectedRow() == -1) {
           JOptionPane.showMessageDialog(null, "Selecione um registro", "Erro", JOptionPane.ERROR_MESSAGE);
           return;
       }
        
        
        if (JOptionPane.showConfirmDialog(null, "Deseja mesmo excluir?", "confirmação", 
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
        
            try {
                licm =(LicencaMaternidade) minhaList.get(tb_func_mat.getSelectedRow());
                dao.excluirLicMat(licm);
                tf_pesq.setText("");
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
         } catch (SQLException ex) {
            Logger.getLogger(FuncionarioForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(LicencaMatForm.class.getName()).log(Level.SEVERE, null, ex);
        }                      
        try {
            preencherTabela();
        } catch (ParseException ex) {
            Logger.getLogger(AdvertenciaForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LicencaMatForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(LicencaMatForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

