    public void preencherTabela() throws ParseException, SQLException, IOException {
        try {
            LicencaMaternidadeDAO dao = new LicencaMaternidadeDAO();
            minhaList = dao.List();
            MyTableModel mtb = new MyTableModel(LicencaMaternidade.class, minhaList, tb_func_mat);
            tb_func_mat.setModel(mtb);
            tb_func_mat.getColumnModel().getColumn(0).setPreferredWidth(300);
            tb_func_mat.getColumnModel().getColumn(1).setPreferredWidth(100);
            tb_func_mat.getColumnModel().getColumn(2).setPreferredWidth(100);
        } catch (SQLException ex) {
            Logger.getLogger(LicencaMaternidade.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }
    
